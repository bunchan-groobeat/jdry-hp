/* =========================================================
   J-DRY サイト設定ファイル（接続情報）
   ---------------------------------------------------------
   Topics（お知らせ）を microCMS から読み込むための接続情報です。
   社長から下記2つの値を受け取ったら、ここを書き換えるだけで接続されます。

   ★ 設定するのは次の2つだけ ★
     1) MICROCMS_SERVICE_DOMAIN … microCMS のサービスドメイン
          例: サービスのURLが https://jdry.microcms.io なら "jdry" の部分
     2) MICROCMS_API_KEY … 「読み取り専用（GETのみ）」のAPIキー

   ⚠️ セキュリティ上とても大切なこと ⚠️
   - このサイトは静的サイトのため、ここに書いたAPIキーは
     閲覧者のブラウザから見える状態になります（技術的に隠せません）。
   - そのため、ここに置いてよいのは必ず「読み取り専用（GET権限のみ）」の
     APIキーだけです。読み取り専用キーは記事を「読む」ことしかできず、
     記事の編集・削除・追加は一切できません（改ざん不可）＝公開しても安全な範囲です。
   - 「書き込み」「管理」権限を含むAPIキーは絶対にここへ書かない／
     GitHubにコミットしないでください。万一漏れると記事を書き換えられます。
========================================================= */

window.JDRY_CONFIG = {
  /* microCMS のサービスドメイン（サブドメイン部分のみ）。未設定なら空のまま。 */
  MICROCMS_SERVICE_DOMAIN: '',

  /* 読み取り専用（GETのみ）のAPIキー。未設定なら空のまま。 */
  MICROCMS_API_KEY: '',

  /* microCMS で作成する API（エンドポイント）のID。スキーマ設計どおり "topics"。 */
  MICROCMS_ENDPOINT: 'topics',

  /* 一覧に表示する最大件数。 */
  TOPICS_LIMIT: 20
};
